    <p><strong>Udaipur, the erstwhile capital of the Mewar region of Rajasthan has a proud heritage as a center for crafts and performing arts.</strong></p>
    <p><br></p>
    <p>Udaipur is famous for its school of miniature paintings in the Rajput - Mughal style. These paintings, meticulous in detailing and delicate in brushwork, are done on cloth, marble, wood or paper. Meenakari, beautifully crafted enamel work is also indigenous to Udaipur. The Meenakari filigree animals and birds are richly coloured in enamel work of ruby reds, blues, greens and whites.&nbsp;</p>
    <p><br></p>
    <p>Bandhini &amp; Lehariya, age old techniques of tying and dyeing fabrics, are another specialty of the region. Traditional silver jewellery and silver artifacts are also available in the jewellery shops and throughout the markets of the old town.&nbsp;</p>
